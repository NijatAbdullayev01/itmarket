const countryByIso2 = new Map(
  COUNTRY_CALLING_CODES.map((country) => [country.iso2, country]),
);

export function getCountryCallingCode(iso2: string) {
  return (
    countryByIso2.get(iso2) ??
    countryByIso2.get(DEFAULT_COUNTRY_ISO2)!
  );
}

export function sortCountriesForPhoneSelect(
  countries: CountryCallingCode[] = COUNTRY_CALLING_CODES,
) {
  const defaultCountry = countries.find(
    (country) => country.iso2 === DEFAULT_COUNTRY_ISO2,
  );
  const rest = countries
    .filter((country) => country.iso2 !== DEFAULT_COUNTRY_ISO2)
    .sort((left, right) => left.iso2.localeCompare(right.iso2));

  return defaultCountry ? [defaultCountry, ...rest] : rest;
}

export const PHONE_COUNTRY_OPTIONS = sortCountriesForPhoneSelect(
  COUNTRY_CALLING_CODES.filter((country) =>
    SUPPORTED_PHONE_COUNTRY_ISO2_CODES.includes(
      country.iso2 as (typeof SUPPORTED_PHONE_COUNTRY_ISO2_CODES)[number],
    ),
  ),
);

export function isoToFlagEmoji(iso2: string) {
  return iso2
    .toUpperCase()
    .replace(/./g, (char) =>
      String.fromCodePoint(127397 + char.charCodeAt(0)),
    );
}
